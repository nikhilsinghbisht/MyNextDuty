package com.mynextduty.core.enums;

public enum Priority {
  CRITICAL,    // Must do immediately (e.g., emergency fund, health insurance)
  HIGH,        // Should do within 3 months (e.g., term insurance, basic investments)
  MEDIUM,      // Should do within 6-12 months (e.g., skill development, career planning)
  LOW,         // Nice to have, can be done later (e.g., hobby development)
  OPTIONAL     // Completely optional based on interest
}